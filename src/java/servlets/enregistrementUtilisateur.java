    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            
            String tel;
            String mail;
            String nom;
            String prenom;
            String statut;
            String matricule;
            int i;
            
            insertionUtilisateur insertion = new insertionUtilisateur();
            RequestDispatcher rd = null;
            
            if(request.getParameter("enregistrerUser")!=null) {
                tel = request.getParameter("telephone");
                nom = request.getParameter("nom");
                prenom = request.getParameter("prenom");
                mail = request.getParameter("mail");
                statut = request.getParameter("statut");
                matricule = request.getParameter("matricule");
                i = insertion.inserer(statut, matricule, nom, prenom, tel, mail);
                
                request.setAttribute("i", i);
                rd = request.getRequestDispatcher("enregistrement.jsp");
            }
            
            rd.forward(request, response);
            
        }
    }

