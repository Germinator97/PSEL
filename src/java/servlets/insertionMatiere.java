    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            
            String code;
            String heure;
            String nom;
            String professeur;
            String classe;
            String contenu;
            int h;
            
            informations.insertionMatiere inserer = new informations.insertionMatiere();
            RequestDispatcher rd = null;
            
            if(request.getParameter("addMatiere")!=null) {
                code = request.getParameter("codeMatiere");
                nom = request.getParameter("matiereMatiere");
                heure = request.getParameter("heureMatiere");
                professeur = request.getParameter("professeurMatiere");
                classe = request.getParameter("classeMatiere");
                contenu = request.getParameter("syllabusMatiere");
                h = inserer.insert(code, nom, heure, professeur, classe, contenu);
                
                request.setAttribute("h", h);
                rd = request.getRequestDispatcher("enregistrement.jsp");
            }
            
            rd.forward(request, response);
            
        }
    }

